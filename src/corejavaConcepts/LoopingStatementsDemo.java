package corejavaConcepts;

public class LoopingStatementsDemo {

	public static void main(String[] args) {
		// Print 1 to 100 numbers

		for (int i = 1; i < 101; i++) {
			System.out.println(i);// 12345678910
		}

		// Print alphabets (A to Z)
		for (char abc = 'A'; abc <= 'Z'; abc++) {//101<=132
			System.out.print(abc);
		}

	}

}
